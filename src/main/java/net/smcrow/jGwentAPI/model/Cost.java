/**
 * Cost Model
 * @author Steve Crow (steve@smcrow.net)
 */
public class Cost {
    private int normal;
    private int premium;

    public int getNormal() {
        return normal;
    }

    public void setNormal(int normal) {
        this.normal = normal;
    }

    public int getPremium() {
        return premium;
    }

    public void setPremium(int premium) {
        this.premium = premium;
    }
}
